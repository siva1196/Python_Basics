import math

# Math module not required for this
x = min(5, 10, 25)
y = max(5, 10, 25)
z = abs(-7.25)
a = pow(4, 3)

print("Minimum",x)
print("Maximum",y)
print("ABS",z)
print("Power",a)

# Math module required fields:
b = math.sqrt(64)
c = math.ceil(1.4)
d = math.floor(1.4)
e = math.pi

print("SQRT",b)
print("Ceil",c) # returns 2
print("Floor",d) # returns 1
print("PI",e)
